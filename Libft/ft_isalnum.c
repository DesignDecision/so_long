#include <stdio.h>
#include "libft.h"

int	ft_isalnum(int c)
{
	int	a;

	a = 1;
	if (((c < 'a' || c > 'z') && (c < 'A' || c > 'Z' )) && (c < '0' || c > '9'))
		a = 0;
	return (a);
}
